class Gallery extends Component {
  state = {
    activeImage: imagesList[0].imageUrl,
    activeImageAltText: imagesList[0].imageAltText,
    activeId: imagesList.id,
  }

  onChangeIMage = (id, imageUrl, imageAltText) => {
    this.setState({activeImage: imageUrl})
    this.setState({activeImageAltText: imageAltText})
    this.setState({activeId: id})
  }

  render() {
    const {activeImage, activeImageAltText, activeId} = this.state

    return (
      <div className="container">
        <img src={activeImage} alt={activeImageAltText} className="image" />
        <h1 className="heading">Nature Photography</h1>
        <div>
          <p className="des">Nature Photography By Rahul</p>
          <ul className="list-items">
            {imagesList.map(each => (
              <ThumbnailItem
                key={each.id}
                EachimageList={each}
                changeImage={this.onChangeIMage}
                isClicked={activeId === each.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
export default Gallery
